public class OperadoresRelacionais {
    public static void main(String[] args) {
        int a = 10;
        int b = 20;
        
        // == Comparação
        System.out.println(a == b);
        // != diferente 
        System.out.println(a != b);
        // < menor 
        System.out.println(a < b);
        // > maior
        System.out.println(a > b);
        // <= menor ou igual
        System.out.println(a <= b);
        // >= maior ou igual 
        System.out.println(a >= b);
        // quando é feito uma operação relacional ele volta um valor booleano
        
    }
    
}
