public class OperadoresAtribuicao {
    public static void main(String[] args) {
        int a = 10; // operador de atribuição 
        int b = a;
        
        b += a; // aditivo 
        b -= a; // subtrativo
        b *= a; // multiplicativo
        b /= a; // diviso
        b %= a; // modular (resto de divisão)
        
    }
}
